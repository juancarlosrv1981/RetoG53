    private void EmployeeList(){
    String queryConsult = "SELECT * FROM Employee";
        try {
            connection = conexion.getConnection();
            st = connection.createStatement();
            rs = st.executeQuery(queryConsult);
            Object[] employee = new Object[6];
            contentEmployeeTable = (DefaultTableModel)tblEmployee.getModel();
            while (rs.next()) {
                employee[0] = rs.getInt("idEmpleado");
                employee[1] = rs.getString("nombreEmpleado");
                employee[2] = rs.getString("apellidoEmpleado");
                employee[3] = rs.getString("tipoDocumento");
                employee[4] = rs.getString("numeroDocumento");
                employee[5] = rs.getString("correoEmpleado");
                contentEmployeeTable.addRow(employee);
                System.out.println("id: " + employee[0] + 
                                   ", Name: " + employee[1] + 
                                   "DocumentType: " + employee[2] + 
                                   " " + employee[3] + 
                                   "Email" + employee[4]);
                tblEmployee.setModel(contentEmployeeTable);
            }
        } catch (SQLException e) {
            System.out.println("Error");
        }
    }
    
    private void borrarDatosTabla(){
        for (int i = 0; i < tblEmployee.getRowCount(); i++) {
            contentEmployeeTable.removeRow(i);
            i-=1;            
        }
    }
