    private void tblEmployeeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmployeeMouseClicked
        int row = tblEmployee.getSelectedRow();
        System.out.println("Fila numero " + row);
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "No ha seleccionado un empelado", "", JOptionPane.WARNING_MESSAGE);     
        }else{
            int id = Integer.parseInt(tblEmployee.getValueAt(row, 0).toString());
            String name = tblEmployee.getValueAt(row, 1).toString();
            String lastName = tblEmployee.getValueAt(row, 2).toString();
            String documentType = tblEmployee.getValueAt(row, 3).toString();
            String documentNumber = tblEmployee.getValueAt(row, 4).toString();
            String email = tblEmployee.getValueAt(row, 5).toString();
            System.out.println(", id: " + id + ", nombre: " + name + " " + lastName + 
                               ", Tipo Documento: " + documentType + ", numero Documento: " + documentNumber + 
                               ", Correo: " + email);
            
            ShowUserForm  showuserform = new ShowUserForm(this, true);
            showuserform.DataReciep(id, name, lastName, documentType, documentNumber, email);
            showuserform.setVisible(true);
            
        }
    }//GEN-LAST:event_tblEmployeeMouseClicked
   
